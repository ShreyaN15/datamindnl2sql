    def _resolve_dimension_phrase_to_column(
        self, phrase: str, columns: List[str]
    ) -> Optional[str]:
        """Map NL fragment after 'by' (e.g. 'role title') to a column name from schema."""
        if not phrase or not columns:
            return None
        stop = {
            "the",
            "a",
            "an",
            "each",
            "every",
            "for",
            "to",
            "of",
            "in",
            "by",
            "and",
            "or",
        }
        words = [
            w.lower()
            for w in phrase.split()
            if w.lower() not in stop and re.match(r"^\w+$", w)
        ]
        if not words:
            return None
        col_lower_map = {c.lower(): c for c in columns}
        snake = "_".join(words)
        if snake in col_lower_map:
            return col_lower_map[snake]
        best = None
        for c in columns:
            cl = c.lower()
            if all(w in cl for w in words):
                if best is None or len(c) < len(best):
                    best = c
        return best

    def _fix_count_by_dimension(
        self, sql: str, question: str, schema_tables: dict
    ) -> str:
        """
        NL asks to count by a dimension but SQL only selects the dimension with no COUNT/GROUP BY.
        Example: Count members by role title -> SELECT role_title FROM members
        """
        qlow = question.lower().strip()
        count_like = bool(
            re.search(r"\bcount\b", qlow)
            or re.search(r"\bhow\s+many\b", qlow)
            or re.search(r"\bnumber\s+of\b", qlow)
        )
        if not count_like or " by " not in f" {qlow} ":
            return sql

        by_match = re.search(r"\bby\s+(.+)$", question.strip(), re.IGNORECASE)
        if not by_match:
            return sql
        dim_phrase = by_match.group(1).strip().rstrip("?.!")
        if not dim_phrase:
            return sql

        had_trailing_semicolon = bool(re.search(r";\s*$", sql))

        sql_upper = sql.upper()
        if "JOIN" in sql_upper or "GROUP BY" in sql_upper or "HAVING" in sql_upper:
            return sql
        if len(re.findall(r"\bFROM\b", sql_upper)) != 1:
            return sql
        if any(a in sql_upper for a in ("COUNT(", "AVG(", "SUM(", "MAX(", "MIN(")):
            return sql

        select_match = re.search(
            r"\bSELECT\s+(.*?)\s+FROM\b", sql, re.IGNORECASE | re.DOTALL
        )
        if not select_match:
            return sql
        select_clause = select_match.group(1).strip()
        if not select_clause or select_clause == "*" or re.match(
            r"^\*\s*,", select_clause
        ):
            return sql
        sel_parts = [p.strip() for p in select_clause.split(",")]
        if len(sel_parts) != 1:
            return sql
        one_sel = sel_parts[0]
        if re.search(r"\b(?:COUNT|AVG|SUM|MAX|MIN)\s*\(", one_sel, re.IGNORECASE):
            return sql

        from_match = re.search(
            r"\bFROM\s+(\w+)(?:\s+(?:AS\s+)?(\w+))?(?=\s*(?:WHERE\b|GROUP\s+BY|ORDER\s+BY|LIMIT\b|HAVING\b|;|\s*$))",
            sql,
            re.IGNORECASE,
        )
        if not from_match:
            return sql
        from_table = from_match.group(1).lower()
        from_alias_sql = (
            from_match.group(2) if from_match.group(2) else from_match.group(1)
        )

        if from_table not in schema_tables:
            return sql
        cols = schema_tables[from_table]
        dim_col = self._resolve_dimension_phrase_to_column(dim_phrase, cols)
        if not dim_col:
            return sql

        base_col = one_sel.split(".")[-1].strip().lower()
        if base_col != dim_col.lower():
            return sql

        qual = f"{from_alias_sql}.{dim_col}"
        new_select = f"{qual}, COUNT(*) AS cnt"
        sql = re.sub(
            r"\bSELECT\s+.*?\s+FROM\b",
            f"SELECT {new_select} FROM",
            sql,
            count=1,
            flags=re.IGNORECASE | re.DOTALL,
        )

        gb = f" GROUP BY {qual}"
        om = re.search(r"\s+ORDER\s+BY\b", sql, re.IGNORECASE)
        lm = re.search(r"\s+LIMIT\b", sql, re.IGNORECASE)
        hm = re.search(r"\s+HAVING\b", sql, re.IGNORECASE)
        if om:
            sql = sql[: om.start()] + gb + sql[om.start() :]
        elif lm:
            sql = sql[: lm.start()] + gb + sql[lm.start() :]
        elif hm:
            sql = sql[: hm.start()] + gb + sql[hm.start() :]
        else:
            sql = sql.rstrip().rstrip(";") + gb
            if had_trailing_semicolon:
                sql += ";"

        logger.info("Added COUNT(*) and GROUP BY for count-by-dimension: %s", qual)
        return sql
    
    def _fix_missing_group_by(self, sql: str, question: str) -> str:
        """
        Add GROUP BY when using aggregation with dimensions.
        
        Pattern: If SELECT has aggregation (COUNT, AVG) and non-aggregated column,
        need GROUP BY.
        """
        sql_upper = sql.upper()
        
        # Check if we have aggregation
        has_agg = any(agg in sql_upper for agg in ['COUNT', 'AVG', 'SUM', 'MAX', 'MIN'])
        if not has_agg:
            return sql
        
        # Check if GROUP BY is missing
        if 'GROUP BY' in sql_upper:
            return sql
        
        # Check if question implies grouping (e.g., "by department", "per student")
        grouping_match = re.search(r'\b(per|each|by)\s+(\w+)', question, re.IGNORECASE)
        if not grouping_match:
            return sql
        
        grouping_keyword = grouping_match.group(1).lower()
        grouping_entity = grouping_match.group(2).lower()
        
        # Find non-aggregated columns in SELECT
        select_match = re.search(r'SELECT\s+(.*?)\s+FROM', sql, re.IGNORECASE | re.DOTALL)
        if not select_match:
            return sql
        
        select_clause = select_match.group(1)
        
        # Find columns that aren't in aggregation functions
        columns = []
        for part in select_clause.split(','):
            part = part.strip()
            if not any(agg in part.upper() for agg in ['COUNT', 'AVG', 'SUM', 'MAX', 'MIN']):
                if part:  # Only non-empty, non-aggregated columns
                    columns.append(part)
        
        # If no dimension columns in SELECT but question has "by X", need to add them
        if not columns:
            # Try to find the grouping table from JOIN
            # Pattern: "by department" → look for departments table in JOIN
            join_match = re.search(rf'JOIN\s+(\w*{grouping_entity}\w*)', sql, re.IGNORECASE)
            if join_match:
                group_table = join_match.group(1).lower()
                # Find name column (look for *_name or name)
                # For now, assume table_name + "_name" or just "name"
                if grouping_entity in group_table:
                    group_col = f"{group_table}.{grouping_entity}_name"
                else:
                    group_col = f"{group_table}.name"
                
                # Add dimension to SELECT and GROUP BY
                select_clause_new = f"{group_col}, {select_clause}"
                sql = re.sub(
                    r'(SELECT\s+)(.*?)(\s+FROM)',
                    rf'\1{select_clause_new}\3',
                    sql,
                    flags=re.IGNORECASE | re.DOTALL,
                    count=1
                )
                columns.append(group_col)
        
        if columns:
            group_by_clause = f" GROUP BY {', '.join(columns)}"
            # Add before WHERE if exists, else at end
            where_match = re.search(r'\s+WHERE', sql, re.IGNORECASE)
            if where_match:
                insert_pos = where_match.start()
                sql = sql[:insert_pos] + group_by_clause + sql[insert_pos:]
            else:
                sql = sql.rstrip() + group_by_clause
        
        return sql
    
