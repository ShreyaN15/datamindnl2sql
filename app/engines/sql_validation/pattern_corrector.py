    def _fix_having_clauses(self, sql: str, question: str) -> str:
        """
        Add HAVING clause when filtering on aggregated results.
        
        Pattern: "more than X", "less than Y", "at least Z" in question with aggregation
        """
        sql_upper = sql.upper()
        
        # Check if we have GROUP BY and aggregation
        if 'GROUP BY' not in sql_upper:
            return sql
        
        has_agg = any(agg in sql_upper for agg in ['COUNT', 'AVG', 'SUM'])
        if not has_agg:
            return sql
        
        # Check if HAVING already exists
        if 'HAVING' in sql_upper:
            return sql
        
        # Check if question implies filtering on aggregation
        filter_pattern = r'(more|greater|less|fewer|at\s+least|at\s+most)\s+than\s+(\d+)'
        match = re.search(filter_pattern, question, re.IGNORECASE)
        
        if match:
            comparator = match.group(1).lower()
            threshold = match.group(2)
            
            # Determine operator
            if 'more' in comparator or 'greater' in comparator:
                operator = '>'
            elif 'less' in comparator or 'fewer' in comparator:
                operator = '<'
            elif 'at least' in comparator:
                operator = '>='
            else:
                operator = '<='
            
            # Find the aggregation function
            agg_match = re.search(r'(COUNT|AVG|SUM)\s*\([^)]+\)', sql, re.IGNORECASE)
            if agg_match:
                agg_function = agg_match.group(0)
                having_clause = f" HAVING {agg_function} {operator} {threshold}"
                
                # Add HAVING after GROUP BY
                group_by_match = re.search(r'(GROUP\s+BY\s+[^;]+)', sql, re.IGNORECASE)
                if group_by_match:
                    insert_pos = group_by_match.end()
                    sql = sql[:insert_pos] + having_clause + sql[insert_pos:]
        
        return sql
    
    def _fix_count_with_unnecessary_groupby(self, sql: str) -> str:
        """
        Remove unnecessary GROUP BY when doing simple COUNT(*).
        
        Pattern: SELECT COUNT(*) FROM table GROUP BY id
        Should be: SELECT COUNT(*) FROM table
        """
        # Only if COUNT(*) is the only thing selected and no dimension
        if re.match(r'SELECT\s+COUNT\(\*\)\s+FROM\s+\w+\s+GROUP\s+BY\s+\w*id\b', sql, re.IGNORECASE):
            sql = re.sub(r'\s+GROUP\s+BY\s+\w*id\b', '', sql, flags=re.IGNORECASE)
        
        return sql


