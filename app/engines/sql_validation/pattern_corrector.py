"""
SQL Pattern Correction Engine

Applies intelligent pattern-based corrections to generated SQL to fix common issues.
Uses generic patterns that work across domains, with special focus on academic/hierarchical
database structures (departments, courses, enrollments, etc.).

This is a hybrid approach: ML generates initial SQL, then pattern-based rules
refine it for correctness.
"""

import os
import re
from collections import deque
from typing import List, Tuple, Optional, Dict
import logging

logger = logging.getLogger(__name__)


class SQLPatternCorrector:
    """
    Applies pattern-based corrections to SQL generated by ML models.
    
    Focuses on fixing common ML errors:
    - Missing JOINs when querying multiple tables
    - Missing GROUP BY when using aggregations with dimensions
    - Incorrect table selection
    - Missing HAVING for filtered aggregations
    """
    
    def __init__(self):
        # Common patterns for entity-attribute relationships
        # Format: (entity_table, attribute_pattern, detail_table)
        self.entity_patterns = [
            # Academic structures
            (r'student', r'department', 'departments'),
            (r'student', r'major', 'departments'),
            (r'professor', r'department', 'departments'),
            (r'course', r'department', 'departments'),
            (r'course', r'professor', 'professors'),
            (r'enrollment', r'student', 'students'),
            (r'enrollment', r'course', 'courses'),
            (r'grade', r'student', 'students'),
            
            # General business patterns
            (r'order', r'customer', 'customers'),
            (r'product', r'category', 'categories'),
            (r'employee', r'department', 'departments'),
            (r'transaction', r'account', 'accounts'),
            (r'borrowing', r'member', 'members'),
            (r'borrowing', r'book', 'books'),
        ]
    
    def _corrector_step_debug_enabled(self) -> bool:
        v = (os.environ.get("NL2SQL_DEBUG_CORRECTOR_STEPS") or "").strip().lower()
        return v in ("1", "true", "yes", "on")

    def _log_corrector_step(self, step_name: str, sql: str) -> None:
        if self._corrector_step_debug_enabled():
            logger.info("NL2SQL_CORRECTOR_STEP[%s]: %s", step_name, sql)

    def correct_sql(
        self,
        sql: str,
        question: str,
        schema_text: str,
        foreign_keys: List[Tuple[str, str, str, str]]
    ) -> str:
        """
        Apply pattern-based corrections to SQL.
        
        Args:
            sql: Generated SQL query
            question: Original natural language question
            schema_text: Schema in text format (will be parsed)
            foreign_keys: List of (from_table, from_col, to_table, to_col)
        
        Returns:
            Corrected SQL query
        """
        original_sql = sql
        
        # Parse schema from text
        schema_tables = self._parse_schema_text(schema_text)
        
        # Build FK map for quick lookup
        fk_map = self._build_fk_map(foreign_keys)
        
        # Apply corrections in order of priority
        sql = self._fix_text_corruption(sql)  # Fix tokenization errors (W JOIN, HERE instead of WHERE)
        self._log_corrector_step("fix_text_corruption", sql)
        sql = self._fix_incomplete_group_by(sql, schema_tables)  # Fix incomplete "GROUP BY" with no column
        self._log_corrector_step("fix_incomplete_group_by", sql)
        sql = self._normalize_string_quotes(sql)  # Normalize double quotes to single quotes for string literals
        self._log_corrector_step("normalize_string_quotes", sql)
        sql = self._fix_nonexistent_columns(sql, schema_tables)  # Fix non-existent column names EARLY
        self._log_corrector_step("fix_nonexistent_columns", sql)
        sql = self._fix_enrolled_students_hallucination(sql, question, schema_tables, fk_map)
        self._log_corrector_step("fix_enrolled_students_hallucination", sql)
        sql = self._fix_select_mismatch_with_question(sql, question, schema_tables)  # Fix SELECT clause when it doesn't match question intent
        self._log_corrector_step("fix_select_mismatch_with_question", sql)
        sql = self._fix_aggregation_instead_of_ordering(sql, question, schema_tables)  # Fix aggregation (AVG/SUM) when ORDER BY is needed
        self._log_corrector_step("fix_aggregation_instead_of_ordering", sql)
        sql = self._fix_table_name_as_column(sql, question, schema_tables, fk_map)  # Fix table name in SELECT
        self._log_corrector_step("fix_table_name_as_column", sql)
        sql = self._fix_select_from_wrong_table(sql, question, schema_tables, fk_map)  # Fix SELECT from wrong table
        self._log_corrector_step("fix_select_from_wrong_table", sql)
        sql = self._remove_incorrect_group_by(sql)  # Remove GROUP BY without aggregation FIRST
        self._log_corrector_step("remove_incorrect_group_by", sql)
        sql = self._fix_having_without_group_by(sql, question)  # Remove/convert invalid HAVING without GROUP BY
        self._log_corrector_step("fix_having_without_group_by", sql)
        sql = self._fix_ambiguous_join_conditions(sql, schema_tables, fk_map)  # Fix ambiguous JOIN conditions (column = column)
        self._log_corrector_step("fix_ambiguous_join_conditions", sql)
        sql = self._fix_incorrect_join_conditions(sql, schema_tables, fk_map)  # Fix wrong JOIN conditions
        self._log_corrector_step("fix_incorrect_join_conditions", sql)
        sql = self._rebuild_students_taught_by_professor_if_needed(
            sql, question, schema_tables, fk_map
        )
        self._log_corrector_step("rebuild_students_taught_by_professor_if_needed", sql)
        sql = self._fix_missing_joins(sql, question, schema_tables, fk_map)
        self._log_corrector_step("fix_missing_joins", sql)
        sql = self._expand_select_for_joined_tables(sql, question, schema_tables)
        self._log_corrector_step("expand_select_for_joined_tables", sql)
        sql = self._fix_where_clause_issues(sql, question, schema_tables, fk_map)  # Fix WHERE column mismatches
        self._log_corrector_step("fix_where_clause_issues", sql)
        sql = self._fix_where_wrong_table(sql, schema_tables)  # Fix WHERE referencing wrong table
        self._log_corrector_step("fix_where_wrong_table", sql)
        sql = self._fix_person_name_in_wrong_column(sql, question, schema_tables, fk_map)  # Fix person name compared to wrong column
        self._log_corrector_step("fix_person_name_in_wrong_column", sql)
        sql = self._fix_fk_string_comparison(sql, schema_tables, fk_map)  # Fix FK compared to string literal
        self._log_corrector_step("fix_fk_string_comparison", sql)
        sql = self._fix_where_missing_column(sql, schema_tables, fk_map)  # Fix WHERE with non-existent alias or column
        self._log_corrector_step("fix_where_missing_column", sql)
        sql = self._fix_date_year_comparison(sql, schema_tables)  # Fix date column compared to year integer
        self._log_corrector_step("fix_date_year_comparison", sql)
        sql = self._fix_spurious_department_name_when_semester_intended(
            sql, question, schema_tables
        )
        self._log_corrector_step("fix_spurious_department_name_when_semester_intended", sql)
        sql = self._add_missing_entity_filter(sql, question, schema_tables, fk_map)  # Add missing WHERE for entity filters
        self._log_corrector_step("add_missing_entity_filter", sql)
        sql = self._add_missing_where_clause(sql, question, schema_tables)  # Add missing WHERE from question
        self._log_corrector_step("add_missing_where_clause", sql)
        sql = self._fix_count_by_dimension(sql, question, schema_tables)
        self._log_corrector_step("fix_count_by_dimension", sql)
        sql = self._fix_missing_group_by(sql, question)
        self._log_corrector_step("fix_missing_group_by", sql)
        sql = self._fix_aggregation_with_dimensions(sql, question, schema_tables, fk_map)
        self._log_corrector_step("fix_aggregation_with_dimensions", sql)
        sql = self._fix_having_clauses(sql, question)
        self._log_corrector_step("fix_having_clauses", sql)
        sql = self._fix_count_with_unnecessary_groupby(sql)
        self._log_corrector_step("fix_count_with_unnecessary_groupby", sql)
        sql = self._remove_unnecessary_joins(sql, schema_tables)  # Remove unnecessary JOINs at the end
        self._log_corrector_step("remove_unnecessary_joins", sql)
        
        if sql != original_sql:
            logger.info(f"Pattern correction applied:\n  Original: {original_sql}\n  Corrected: {sql}")
        
        return sql
    
    def _parse_schema_text(self, schema_text: str) -> dict:
        """Parse schema text into dict of table_name -> [columns]"""
        schema_tables = {}
        current_table = None
        
        for line in schema_text.split('\n'):
            line = line.strip()
            if not line:
                continue
            
            # Skip schema header lines (e.g., "SCHEMA FOR student_db:")
            if line.upper().startswith('SCHEMA FOR') or line.upper().startswith('SCHEMA:'):
                continue
            
            # Check if it's a table definition line
            # Format 1: "TABLE table_name:" or "table_name:"
            if ':' in line and not line.startswith('-'):
                parts = line.split(':', 1)
                table_name = parts[0].strip().lower()
                
                # Strip "table " prefix if present
                if table_name.startswith('table '):
                    table_name = table_name[6:]  # Remove "table "
                
                # Initialize table with empty column list
                schema_tables[table_name] = []
                current_table = table_name
                
                # Check if columns are on the same line (Format: "table: col1, col2, col3")
                columns_part = parts[1].strip() if len(parts) > 1 else ""
                if columns_part and not columns_part.startswith('-'):
                    # Extract column names (remove type info and FK annotations)
                    columns = []
                    for col in columns_part.split(','):
                        col = col.strip()
                        # Remove type info in parentheses
                        col = re.sub(r'\([^)]*\)', '', col)
                        # Take first word as column name
                        col_name = col.split()[0] if col else ""
                        if col_name:
                            columns.append(col_name.lower())
                    schema_tables[table_name] = columns
            
            # Format 2: "- table.column" or "- column"
            elif line.startswith('-'):
                # Extract column name from "- table.column" or "- column"
                col_text = line[1:].strip()  # Remove '-' prefix
                
                # Remove FK annotation: "(references ...)" or "(FK to ...)"
                col_text = re.sub(r'\((?:references|FK to)[^)]*\)', '', col_text).strip()
                # Remove other annotations in parentheses like (PK)
                col_text = re.sub(r'\([^)]*\)', '', col_text).strip()
                
                # Handle "table.column" format
                if '.' in col_text:
                    parts = col_text.split('.', 1)
                    table_name = parts[0].strip().lower()
                    col_name = parts[1].split()[0] if parts[1] else ""  # Take just the column name
                    
                    # Initialize table if not exists
                    if table_name not in schema_tables:
                        schema_tables[table_name] = []
                    
                    if col_name:
                        schema_tables[table_name].append(col_name.lower())
                elif current_table:
                    # Format: "- column" when current_table is set
                    col_name = col_text.split()[0]  # Take first word
                    if col_name:
                        schema_tables[current_table].append(col_name.lower())
        
        logger.info(f"Parsed schema tables: {list(schema_tables.keys())}")
        return schema_tables
    
    def _build_fk_map(self, foreign_keys: List[Tuple[str, str, str, str]]) -> dict:
        """Build a map of table relationships"""
        fk_map = {}
        for from_table, from_col, to_table, to_col in foreign_keys:
            if from_table not in fk_map:
                fk_map[from_table] = []
            fk_map[from_table].append({
                'from_col': from_col,
                'to_table': to_table,
                'to_col': to_col
            })
        return fk_map

    def _prefer_enrollment_bridge_students_courses(
        self, start: str, goal: str, question_lower: str
    ) -> bool:
        """Prefer paths through enrollments for students↔courses when NL implies class-taking."""
        pair = {start.lower(), goal.lower()}
        if pair != {"students", "courses"}:
            return False
        signals = (
            r"\benrolled\b",
            r"\btaking\b",
            r"\btaught\s+by\b",
            r"\bstudent",
            r"\bcourses?\b",
        )
        return any(re.search(p, question_lower) for p in signals)

    def _bfs_join_path_steps(
        self,
        start_table: str,
        goal_table: str,
        fk_map: dict,
        prefer_enrollment_students_courses: bool = False,
    ) -> Optional[List[dict]]:
        """
        Shortest undirected join path using FK metadata only.
        Each step: JOIN step['table'] ON step['from_table'].from_col = step['table'].to_col
        """
        start = start_table.lower()
        goal = goal_table.lower()
        if start == goal:
            return []

        parent: Dict[str, Tuple[Optional[str], Optional[dict]]] = {start: (None, None)}
        q = deque([start])

        while q:
            cur = q.popleft()
            if cur == goal:
                steps: List[dict] = []
                at = goal
                while at != start:
                    prev, step = parent[at]
                    if prev is None:
                        return None
                    if step:
                        steps.append(step)
                    at = prev
                return list(reversed(steps))

            forward_edges: List[Tuple[str, dict]] = []
            for fk in fk_map.get(cur, []):
                nxt = fk["to_table"].lower()
                forward_edges.append(
                    (
                        nxt,
                        {
                            "table": nxt,
                            "from_table": cur,
                            "from_col": fk["from_col"],
                            "to_col": fk["to_col"],
                        },
                    )
                )

            incoming_edges: List[Tuple[str, dict]] = []
            for from_t, fks in fk_map.items():
                ft = from_t.lower()
                for fk in fks:
                    if fk["to_table"].lower() != cur:
                        continue
                    nxt = ft
                    incoming_edges.append(
                        (
                            nxt,
                            {
                                "table": nxt,
                                "from_table": cur,
                                "from_col": fk["from_col"],
                                "to_col": fk["to_col"],
                            },
                        )
                    )

            use_incoming_first = prefer_enrollment_students_courses and cur in (
                "students",
                "courses",
            )
            ordered = incoming_edges + forward_edges if use_incoming_first else forward_edges + incoming_edges

            for nxt, step in ordered:
                if nxt not in parent:
                    parent[nxt] = (cur, step)
                    q.append(nxt)
        return None

    def _parse_instructor_display_name_from_question(self, question: str) -> Optional[str]:
        """Extract instructor name after taught by / professor / instructor."""
        patterns = (
            r"\btaught\s+by\s+([A-Za-z]+(?:\s+[A-Za-z]+)+)(?=\s*[?.!]|$)",
            r"\btaught\s+by\s+([A-Za-z]+)(?=\s*[?.!]|$)",
            r"\bprofessor\s+([A-Za-z]+(?:\s+[A-Za-z]+)+)\b",
            r"\bprofessor\s+([A-Za-z]+)\b",
            r"\binstructor\s+([A-Za-z]+(?:\s+[A-Za-z]+)+)\b",
            r"\binstructor\s+([A-Za-z]+)\b",
        )
        for pat in patterns:
            m = re.search(pat, question.strip(), re.IGNORECASE)
            if m:
                name = m.group(1).strip()
                if name.lower() not in ("the", "a", "an", "named", "called"):
                    return name
        return None

    def _professor_where_clause_from_name(self, name: str) -> str:
        """Build professors p WHERE clause; escape single quotes in literals."""
        safe = name.replace("'", "''")
        parts = safe.split()
        if len(parts) >= 2:
            return f"p.first_name = '{parts[0]}' AND p.last_name = '{' '.join(parts[1:])}'"
        return f"p.last_name = '{parts[0]}'"

    def _canonical_students_in_professor_courses_sql(self, where_prof: str) -> str:
        return (
            "SELECT s.first_name, s.last_name, c.course_name "
            "FROM students s "
            "JOIN enrollments e ON s.student_id = e.student_id "
            "JOIN courses c ON e.course_id = c.course_id "
            "JOIN professors p ON c.professor_id = p.professor_id "
            f"WHERE {where_prof}"
        )

    def _rebuild_students_taught_by_professor_if_needed(
        self,
        sql: str,
        question: str,
        schema_tables: dict,
        fk_map: dict,
    ) -> str:
        """
        Replace broken student+professor join paths before generic JOIN injection.
        Handles 'List students taking courses taught by Alan Turing' (no word 'professor').
        """
        ql = question.lower()
        if not re.search(r"\bstudent", ql):
            return sql
        if not (
            re.search(r"\btaught\s+by\b", ql)
            or re.search(r"\b(?:professor|instructor)\b", ql)
        ):
            return sql
        required = ("students", "enrollments", "courses", "professors")
        if not all(t in schema_tables for t in required):
            return sql
        name = self._parse_instructor_display_name_from_question(question)
        if not name:
            return sql
        low = sql.lower()
        if "professors" in low and "enrollments" in low and re.search(
            r"p\.\s*(?:first_name|last_name)\s*=", sql, re.IGNORECASE
        ):
            return sql
        where_p = self._professor_where_clause_from_name(name)
        fixed = self._canonical_students_in_professor_courses_sql(where_p)
        logger.info("Rebuilt students + taught-by-professor query from NL intent")
        return fixed

    def _fix_enrolled_students_hallucination(
        self,
        sql: str,
        question: str,
        schema_tables: dict,
        fk_map: dict,
    ) -> str:
        """Fix SELECT enrolled_students FROM courses ... for enrollment-by-code questions."""
        if not re.search(r"\benrolled_students\b", sql, re.IGNORECASE):
            return sql
        ql = question.lower()
        if not (
            re.search(r"\bstudent", ql)
            and (
                re.search(r"\benrolled\b", ql)
                or re.search(r"\benrollment\b", ql)
                or re.search(r"\btaking\b", ql)
            )
        ):
            return sql
        required = ("students", "enrollments", "courses")
        if not all(t in schema_tables for t in required):
            return sql
        m_q = re.search(
            r"\b(?:course\s+code|code)\s+([A-Z]{2,6}\d{2,4})\b", question, re.IGNORECASE
        )
        m_sql = re.search(r"course_code\s*=\s*'([^']+)'", sql, re.IGNORECASE)
        code = (m_q.group(1) if m_q else None) or (m_sql.group(1) if m_sql else None)
        if not code:
            return sql
        code_esc = code.replace("'", "''")
        fixed = (
            "SELECT s.first_name, s.last_name "
            "FROM students s "
            "JOIN enrollments e ON s.student_id = e.student_id "
            "JOIN courses c ON e.course_id = c.course_id "
            f"WHERE c.course_code = '{code_esc}'"
        )
        logger.info("Fixed enrolled_students hallucination with enrollment join chain")
        return fixed
    
    def _fix_text_corruption(self, sql: str) -> str:
        """
        Fix text corruption/tokenization errors in generated SQL.
        
        Common issues:
        - "W JOIN" instead of "WHERE ... JOIN"
        - "HERE" appearing as separate word instead of part of WHERE
        - Split keywords due to tokenization
        - Merged text like "department_idROUP BY" (should be "department_id GROUP BY")
        - Standalone letters from split keywords like "students G JOIN"
        
        Examples:
        Wrong: SELECT * FROM table W JOIN other ON ...HERE condition = value
        Right: SELECT * FROM table JOIN other ON ... WHERE condition = value
        
        Wrong: SELECT COUNT(*) FROM students G JOIN departments ON id = idROUP BY name
        Right: SELECT COUNT(*) FROM students JOIN departments ON id = id GROUP BY name
        """
        original_sql = sql
        
        # Protect valid keywords before fixing merged text (to avoid matching inside them)
        # Protect GROUP BY
        sql = re.sub(r'\bGROUP\s+BY\b', '__PROTECTED_GROUP_BY__', sql, flags=re.IGNORECASE)
        # Protect ORDER BY
        sql = re.sub(r'\bORDER\s+BY\b', '__PROTECTED_ORDER_BY__', sql, flags=re.IGNORECASE)
        
        # Fix merged text patterns (keyword fragments merged with previous word)
        # Pattern: word+ROUP BY → word GROUP BY (e.g., "department_idROUP BY" → "department_id GROUP BY")
        sql = re.sub(r'(\w)ROUP\s+BY\b', r'\1 GROUP BY', sql, flags=re.IGNORECASE)
        
        # Pattern: word+RDER BY → word ORDER BY (e.g., "columnORDER BY" → "column ORDER BY")
        sql = re.sub(r'(\w)RDER\s+BY\b', r'\1 ORDER BY', sql, flags=re.IGNORECASE)
        
        # Restore protected keywords
        sql = re.sub(r'__PROTECTED_GROUP_BY__', 'GROUP BY', sql)
        sql = re.sub(r'__PROTECTED_ORDER_BY__', 'ORDER BY', sql)
        
        # Fix standalone single letters before keywords (from split keywords)
        # Pattern: table G JOIN → table JOIN (G is fragment from GROUP)
        sql = re.sub(r'\b(\w+)\s+G\s+JOIN\b', r'\1 JOIN', sql, flags=re.IGNORECASE)
        
        # Pattern: table O WHERE → table WHERE (O is fragment from ORDER or other keyword)
        sql = re.sub(r'\b(\w+)\s+O\s+WHERE\b', r'\1 WHERE', sql, flags=re.IGNORECASE)
        
        # Fix duplicate keywords (resulting from split/merge issues)
        # Pattern: GROUP BY GROUP BY → GROUP BY
        sql = re.sub(r'\bGROUP\s+BY\s+GROUP\s+BY\b', 'GROUP BY', sql, flags=re.IGNORECASE)
        
        # Pattern: ORDER BY ORDER BY → ORDER BY
        sql = re.sub(r'\bORDER\s+BY\s+ORDER\s+BY\b', 'ORDER BY', sql, flags=re.IGNORECASE)
        
        # Fix "W JOIN" - should be just "JOIN" (W is fragment of WHERE that got misplaced)
        sql = re.sub(r'\bW\s+JOIN\b', 'JOIN', sql, flags=re.IGNORECASE)
        
        # Fix split keywords (but be careful not to match WHERE as "W HERE")
        # Only fix if there's clear evidence of splitting (extra spaces, position after punctuation, etc.)
        # Fix "FR OM" → "FROM"
        sql = re.sub(r'\bFR\s+OM\b', 'FROM', sql, flags=re.IGNORECASE)
        # Fix "JO IN" → "JOIN"  
        sql = re.sub(r'\bJO\s+IN\b', 'JOIN', sql, flags=re.IGNORECASE)
        # Fix "SELE CT" → "SELECT"
        sql = re.sub(r'\bSELE\s+CT\b', 'SELECT', sql, flags=re.IGNORECASE)
        # Fix "GRO UP" → "GROUP"
        sql = re.sub(r'\bGRO\s+UP\b', 'GROUP', sql, flags=re.IGNORECASE)
        # Fix "ORDE R" → "ORDER"
        sql = re.sub(r'\bORDE\s+R\b', 'ORDER', sql, flags=re.IGNORECASE)
        
        # Fix "HERE" corruption - but NOT if it's part of WHERE
        # Strategy: Temporarily replace WHERE with a safe placeholder that won't match our patterns
        # Use a placeholder that doesn't contain word characters followed by HERE
        sql_temp = re.sub(r'\bWHERE\b', '__SAFE_WHERE_PLACEHOLDER__', sql, flags=re.IGNORECASE)
        
        # Now fix HERE patterns (WHERE is protected)
        # Pattern 1: "...idHERE column" → "...id WHERE column"
        sql_temp = re.sub(r'(\w)HERE\b', r'\1 WHERE', sql_temp, flags=re.IGNORECASE)
        # Pattern 2: Standalone "HERE"
        sql_temp = re.sub(r'\bHERE\b', 'WHERE', sql_temp, flags=re.IGNORECASE)
        
        # Restore protected WHERE instances
        sql = re.sub(r'__SAFE_WHERE_PLACEHOLDER__', 'WHERE', sql_temp)
        
        # Fix "FR OM" (FROM split)
        sql = re.sub(r'\bFR\s+OM\b', 'FROM', sql, flags=re.IGNORECASE)
        
        # Fix "JO IN" (JOIN split)
        sql = re.sub(r'\bJO\s+IN\b', 'JOIN', sql, flags=re.IGNORECASE)
        
        # Fix "SELE CT" (SELECT split)
        sql = re.sub(r'\bSELE\s+CT\b', 'SELECT', sql, flags=re.IGNORECASE)
        
        # Fix "GRO UP BY" (GROUP BY split)
        sql = re.sub(r'\bGRO\s+UP\s+BY\b', 'GROUP BY', sql, flags=re.IGNORECASE)
        
        # Fix "ORDE R BY" (ORDER BY split)
        sql = re.sub(r'\bORDE\s+R\s+BY\b', 'ORDER BY', sql, flags=re.IGNORECASE)
        
        if sql != original_sql:
            logger.info(f"Fixed text corruption: '{original_sql}' → '{sql}'")
        
        return sql
    
    def _fix_incomplete_group_by(self, sql: str, schema_tables: dict) -> str:
        """
        Fix incomplete GROUP BY clause (GROUP BY with no column specified).
        
        This happens when ML model generates: SELECT COUNT(*), dept_name FROM table GROUP BY
        Should be: SELECT COUNT(*), dept_name FROM table GROUP BY dept_name
        
        Strategy:
        1. Detect "GROUP BY" followed by end of string or keyword (WHERE/ORDER/LIMIT/;)
        2. Find non-aggregated columns in SELECT
        3. Add those columns to GROUP BY
        """
        original_sql = sql
        
        # Check if GROUP BY exists and is incomplete
        # Pattern: GROUP BY followed immediately by end/keyword (not a column name)
        incomplete_group_by = re.search(r'GROUP\s+BY\s*(?:WHERE|ORDER|LIMIT|HAVING|;|$)', sql, re.IGNORECASE)
        
        if not incomplete_group_by:
            return sql
        
        logger.info(f"Detected incomplete GROUP BY clause")
        
        # Extract SELECT clause
        select_match = re.search(r'SELECT\s+(.*?)\s+FROM', sql, re.IGNORECASE | re.DOTALL)
        if not select_match:
            return sql
        
        select_clause = select_match.group(1)
        
        # Find non-aggregated columns  in SELECT
        # Remove aggregation functions to find regular columns
        select_without_agg = re.sub(r'(?:COUNT|SUM|AVG|MAX|MIN|DISTINCT)\s*\([^)]*\)', '', select_clause, flags=re.IGNORECASE)
        
        # Extract column references (table.column or just column)
        columns = re.findall(r'(\w+\.\w+|\w+)', select_without_agg)
        
        # Filter out SQL keywords and find actual columns
        sql_keywords = {'as', 'from', 'where', 'select', 'join', 'on', 'and', 'or', 'not', 'in', 'like'}
        group_by_columns = [col for col in columns if col.lower() not in sql_keywords and col.strip()]
        
        if not group_by_columns:
            # No columns to group by - remove GROUP BY entirely
            logger.info(f"No grouping columns found, removing GROUP BY")
            sql = re.sub(r'\s+GROUP\s+BY\s*(?:WHERE|ORDER|LIMIT|HAVING|;|$)', r' \1', sql, flags=re.IGNORECASE)
            return sql
        
        # Add columns to GROUP BY
        group_by_clause = 'GROUP BY ' + ', '.join(group_by_columns)
        
        # Replace incomplete GROUP BY with complete one
        sql = re.sub(r'GROUP\s+BY\s*(?=(WHERE|ORDER|LIMIT|HAVING|;|$))', group_by_clause + ' ', sql, flags=re.IGNORECASE)
        
        if sql != original_sql:
            logger.info(f"Fixed incomplete GROUP BY: '{original_sql}' → '{sql}'")
        
        return sql
    
    def _normalize_string_quotes(self, sql: str) -> str:
        """
        Normalize string literal quotes to use single quotes instead of double quotes.
        
        SQL standard: String literals should use single quotes 'value'
        Some systems accept double quotes "value" but this can be ambiguous with identifier quotes
        
        Example:
        Wrong: WHERE department_name = "Computer Science"
        Right: WHERE department_name = 'Computer Science'
        
        Strategy:
        1. Find all double-quoted strings (not followed by table/column context)
        2. Convert to single quotes
        3. Handle escaped quotes inside strings
        """
        original_sql = sql
        
        # Pattern: Match double-quoted strings that look like string literals (not identifiers)
        # Avoid matching things after AS which are likely aliases
        # Match "string" but not immediately after AS or as part of identifier context
        
        # Find all double-quoted strings
        # Pattern: "anything inside double quotes"
        pattern = r'"([^"]*)"'
        
        matches = list(re.finditer(pattern, sql))
        
        if not matches:
            return sql
        
        # Process matches from end to start to maintain positions
        for match in reversed(matches):
            full_match = match.group(0)  # "value"
            inner_content = match.group(1)  # value
            start_pos = match.start()
            end_pos = match.end()
            
            # Check if this is likely a string literal (not an identifier)
            # Look at context before the quote
            context_before = sql[max(0, start_pos-20):start_pos].strip().upper()
            
            # Double quotes after = or IN are likely string literals
            is_string_literal = any(op in context_before for op in ['=', 'IN', 'LIKE', 'ILIKE', '!=', '<>', '>', '<', '>=', '<='])
            
            # If it contains spaces or special chars, it's likely a string literal
            if ' ' in inner_content or any(c in inner_content for c in [',', '.', '!', '?', '-', '/']):
                is_string_literal = True
            
            # Convert to single quotes if it's a string literal
            if is_string_literal:
                # Escape any single quotes in the content
                escaped_content = inner_content.replace("'", "''")
                # Replace with single-quoted version
                sql = sql[:start_pos] + f"'{escaped_content}'" + sql[end_pos:]
        
        if sql != original_sql:
            logger.info(f"Normalized string quotes: '{original_sql}' → '{sql}'")
        
        return sql
    
    def _fix_aggregation_instead_of_ordering(self, sql: str, question: str, schema_tables: dict) -> str:
        """
        Fix queries that incorrectly use aggregation (AVG, SUM, COUNT, MIN, MAX) when they should use ORDER BY + LIMIT.
        
        This handles "top N", "bottom N", "highest", "lowest", "best", "worst" type queries.
        
        Example:
        Question: "Top 5 students by GPA"
        Wrong: SELECT AVG(gpa) FROM students
        Fixed: SELECT first_name, last_name, gpa FROM students ORDER BY gpa DESC LIMIT 5
        """
        original_sql = sql
        question_lower = question.lower()
        sql_upper = sql.upper()
        
        # Detect ranking/ordering patterns in the question
        ranking_patterns = [
            (r'\btop\s+(\d+)?', 'DESC'),  # "top 5" or "top"
            (r'\bbottom\s+(\d+)?', 'ASC'),  # "bottom 3" or "bottom"
            (r'\bhighest\s+(\d+)?', 'DESC'),  # "highest" or "highest 10"
            (r'\blowest\s+(\d+)?', 'ASC'),  # "lowest" or "lowest 5"
            (r'\bbest\s+(\d+)?', 'DESC'),  # "best" or "best 3"
            (r'\bworst\s+(\d+)?', 'ASC'),  # "worst" or "worst 2"
        ]
        
        limit_value = None
        order_direction = None
        
        for pattern, direction in ranking_patterns:
            match = re.search(pattern, question_lower)
            if match:
                order_direction = direction
                # Extract N value if present
                if match.groups():
                    try:
                        limit_value = int(match.group(1)) if match.group(1) else 1
                    except (ValueError, TypeError):
                        limit_value = 1
                else:
                    limit_value = 1
                break
        
        # If no ranking pattern found, return original SQL
        if not order_direction:
            return sql
        
        # Check if SQL uses aggregation without GROUP BY (indicates incorrect aggregation)
        has_aggregation = bool(re.search(r'\b(AVG|SUM|COUNT|MIN|MAX)\s*\(', sql_upper))
        has_group_by = bool(re.search(r'\bGROUP\s+BY\b', sql_upper))
        
        # If no aggregation or has GROUP BY (legitimate use), return original
        if not has_aggregation or has_group_by:
            return sql
        
        # Extract FROM table
        from_match = re.search(r'FROM\s+(\w+)', sql, re.IGNORECASE)
        if not from_match:
            return sql
        
        from_table = from_match.group(1).lower()
        
        # Determine entity being queried
        entity = None
        if 'student' in question_lower:
            entity = 'students'
        elif 'professor' in question_lower or 'teacher' in question_lower or 'instructor' in question_lower:
            entity = 'professors'
        elif 'course' in question_lower or 'class' in question_lower:
            entity = 'courses'
        elif 'department' in question_lower:
            entity = 'departments'
        elif 'grade' in question_lower or 'score' in question_lower:
            entity = 'grades'
        elif 'enrollment' in question_lower:
            entity = 'enrollments'
        
        # If entity not detected, try using the FROM table
        if not entity:
            entity = from_table
        
        # Determine attribute to order by from question
        # Look for "by <attribute>" pattern
        by_attribute = None
        by_match = re.search(r'\bby\s+(\w+(?:\s+\w+)?)', question_lower)
        if by_match:
            attr_phrase = by_match.group(1).strip()
            # Map common phrases to column names
            attr_mappings = {
                'gpa': 'gpa',
                'grade': 'grade_value',
                'score': 'grade_value',
                'salary': 'salary',
                'age': 'age',
                'enrollment': 'enrollment_count',
                'students': 'student_count',
                'name': 'last_name',
            }
            by_attribute = attr_mappings.get(attr_phrase, attr_phrase)
        
        # If no "by" attribute found, try to extract from SELECT aggregation
        if not by_attribute:
            agg_match = re.search(r'(AVG|SUM|COUNT|MIN|MAX)\s*\(\s*(\w+)\s*\)', sql, re.IGNORECASE)
            if agg_match:
                by_attribute = agg_match.group(2)
        
        # If still no attribute, return original
        if not by_attribute:
            return sql
        
        # Build proper SELECT clause based on entity
        select_cols = []
        
        # Add identifier columns
        if entity in ['students', 'professors']:
            select_cols.extend(['first_name', 'last_name'])
        elif entity == 'courses':
            select_cols.append('course_name')
        elif entity == 'departments':
            select_cols.append('department_name')
        elif entity == 'grades':
            select_cols.append('grade_id')
        elif entity == 'enrollments':
            select_cols.append('enrollment_id')
        
        # Add the ordering attribute if not already included
        if by_attribute and by_attribute not in select_cols:
            select_cols.append(by_attribute)
        
        # Build new SQL
        select_clause = ', '.join(select_cols)
        new_sql = f"SELECT {select_clause} FROM {entity} ORDER BY {by_attribute} {order_direction}"
        
        if limit_value:
            new_sql += f" LIMIT {limit_value}"
        
        # Preserve WHERE clause if present
        where_match = re.search(r'(WHERE\s+.+?)(?:\s+ORDER\s+BY|\s+GROUP\s+BY|\s+LIMIT|$)', sql, re.IGNORECASE)
        if where_match:
            where_clause = where_match.group(1).strip()
            # Insert WHERE before ORDER BY
            new_sql = f"SELECT {select_clause} FROM {entity} {where_clause} ORDER BY {by_attribute} {order_direction}"
            if limit_value:
                new_sql += f" LIMIT {limit_value}"
        
        if new_sql != original_sql:
            logger.info(f"Fixed aggregation→ordering: '{original_sql}' → '{new_sql}'")
        
        return new_sql
    
    def _fix_table_name_as_column(self, sql: str, question: str, schema_tables: dict, fk_map: dict) -> str:
        """
        Fix queries that use table names as column names in SELECT.
        
        Example:
        Wrong: SELECT students FROM departments WHERE department_name = 'CS'
        Issues: 1) 'students' is table name not column
                2) FROM departments is wrong (should be FROM students)
                3) Need JOIN to filter by department
        
        Correct: SELECT s.* FROM students s 
                 JOIN departments d ON s.department_id = d.department_id
                 WHERE d.department_name = 'CS'
        """
        # Extract SELECT clause
        select_match = re.search(r'SELECT\s+(.+?)\s+FROM\s+(\w+)', sql, re.IGNORECASE)
        if not select_match:
            return sql
        
        select_clause = select_match.group(1).strip()
        from_table = select_match.group(2).lower()
        
        # Check if SELECT clause is a table name (not a column)
        if select_clause.lower() in schema_tables.keys():
            # This is a table name being used as column!
            target_table = select_clause.lower()
            logger.info(f"Detected table name '{target_table}' used as column in SELECT")
            
            # Determine if we need to switch FROM table
            # Pattern: "students in X department" or "students from X"
            if re.search(r'\bin\s+(?:the\s+)?(\w+(?:\s+\w+)?)\s+department', question, re.IGNORECASE):
                # Query is about students, but filtered by department
                # Need: FROM students JOIN departments WHERE department_name = X
                
                # Check if FROM table is correct
                if from_table != target_table:
                    # Wrong FROM table - rebuild query
                    return self._rebuild_filtered_query(sql, question, target_table, from_table, schema_tables, fk_map)
        
        # Check if SELECT has table name that doesn't match FROM table
        select_parts = [p.strip() for p in select_clause.split(',')]
        for part in select_parts:
            # Remove aliases and functions
            col_name = re.sub(r'\s+as\s+\w+', '', part, flags=re.IGNORECASE)
            col_name = col_name.split('.')[-1].strip()
            
            # Is this a table name?
            if col_name.lower() in schema_tables.keys() and col_name.lower() != from_table:
                logger.info(f"Detected table name '{col_name}' in SELECT but FROM is '{from_table}'")
                return self._rebuild_filtered_query(sql, question, col_name.lower(), from_table, schema_tables, fk_map)
        
        return sql
    
    def _rebuild_filtered_query(self, sql: str, question: str, target_table: str, 
                                 filter_table: str, schema_tables: dict, fk_map: dict) -> str:
        """
        Rebuild query when wrong table used in FROM but correct filter in WHERE.
        
        Example: SELECT students FROM departments WHERE department_name = 'CS'
        Should be: SELECT s.* FROM students s JOIN departments d ... WHERE d.department_name = 'CS'
        """
        # Extract WHERE clause to preserve filter
        where_match = re.search(r'WHERE\s+(.+?)(?:GROUP|ORDER|LIMIT|;|$)', sql, re.IGNORECASE)
        where_clause = where_match.group(1).strip() if where_match else None
        
        # Get columns from target table
        target_cols = schema_tables.get(target_table, [])
        if not target_cols:
            return sql
        
        # Build SELECT clause - use * or common display columns
        display_cols = []
        for col in target_cols:
            if any(keyword in col.lower() for keyword in ['name', 'title', 'email']):
                display_cols.append(f"{target_table[0]}.{col}")
        
        if not display_cols:
            select_clause = f"{target_table[0]}.*"
        else:
            select_clause = ', '.join(display_cols[:3])  # Limit to 3 display columns
        
        # Find JOIN path from target_table to filter_table
        # First try direct FK relationship
        fk_rel = self._find_fk_relationship(target_table, filter_table, fk_map)
        
        if fk_rel:
            # Direct relationship exists
            join_clause = f" JOIN {filter_table} {filter_table[0]} ON {target_table[0]}.{fk_rel['from_col']} = {filter_table[0]}.{fk_rel['to_col']}"
        else:
            # Try multi-hop path
            join_path = self._find_join_path(
                target_table, filter_table, fk_map, set(), question.lower()
            )
            
            if not join_path:
                # No JOIN path found - just fix FROM table
                if where_clause:
                    # Check if WHERE clause references filter_table columns
                    # If so, we can't help without JOIN
                    logger.warning(f"No JOIN path from {target_table} to {filter_table}")
                    return sql
                new_sql = f"SELECT {select_clause} FROM {target_table} {target_table[0]}"
                return new_sql
            
            # Build JOIN clause from path
            join_clause = ""
            for step in join_path:
                from_alias = step['from_table'][0]
                to_alias = step['table'][0]
                join_clause += f" JOIN {step['table']} {to_alias} ON {from_alias}.{step['from_col']} = {to_alias}.{step['to_col']}"
        
        # Fix WHERE clause to use correct alias
        if where_clause:
            # Replace table name or no prefix with correct alias
            # Example: "department_name = 'CS'" → "d.department_name = 'CS'"
            filter_cols = schema_tables.get(filter_table, [])
            for col in filter_cols:
                # Match column without alias or with wrong alias
                where_clause = re.sub(
                    rf'\b{col}\b',
                    f"{filter_table[0]}.{col}",
                    where_clause,
                    flags=re.IGNORECASE
                )
        
        # Construct final query
        new_sql = f"SELECT {select_clause} FROM {target_table} {target_table[0]}{join_clause}"
        if where_clause:
            new_sql += f" WHERE {where_clause}"
        
        logger.info(f"Rebuilt query: {new_sql}")
        return new_sql
    
    def _fix_select_from_wrong_table(self, sql: str, question: str, schema_tables: dict, fk_map: dict) -> str:
        """
        Fix queries where SELECT columns don't exist in the FROM table.
        
        Example:
        Wrong: SELECT student_id FROM departments WHERE department_name = 'CS'
        Issue: student_id exists in students table, not departments table
        Fix: SELECT student_id FROM students JOIN departments ON ... WHERE department_name = 'CS'
        
        Strategy:
        1. Extract SELECT columns and FROM table
        2. Check if SELECT columns exist in FROM table
        3. If not, find which table they belong to
        4. Rebuild query with correct FROM table and JOIN
        """
        # Extract SELECT and FROM
        select_match = re.search(r'SELECT\s+(.+?)\s+FROM\s+(\w+)', sql, re.IGNORECASE)
        if not select_match:
            return sql
        
        select_clause = select_match.group(1).strip()
        from_table = select_match.group(2).lower()
        
        # Skip if SELECT has *, COUNT, or other aggregations
        if '*' in select_clause or any(agg in select_clause.upper() for agg in ['COUNT(', 'AVG(', 'SUM(', 'MAX(', 'MIN(']):
            return sql
        
        # Get columns from FROM table
        if from_table not in schema_tables:
            return sql
        
        from_table_cols = [c.lower() for c in schema_tables[from_table]]
        
        # Extract column names from SELECT (handle aliases, functions, etc.)
        select_cols = []
        select_for_split = re.sub(
            r'^\s*DISTINCT\s+',
            '',
            select_clause,
            flags=re.IGNORECASE,
        )
        for part in select_for_split.split(','):
            # Remove table prefix if exists (T1.column → column)
            col = part.strip()
            col = re.sub(r'^\s*DISTINCT\s+', '', col, flags=re.IGNORECASE)
            col = re.sub(r'^\w+\.', '', col)  # Remove alias prefix
            col = re.sub(r'\s+as\s+\w+', '', col, flags=re.IGNORECASE)  # Remove AS alias
            col = col.strip()
            if col and not col.isdigit():
                select_cols.append(col.lower())
        
        # Check if any SELECT column doesn't exist in FROM table
        missing_cols = [col for col in select_cols if col not in from_table_cols]
        
        if not missing_cols:
            # All columns exist in FROM table - no fix needed
            return sql
        
        # Find which table(s) contain the missing columns
        # Strategy: Prioritize tables where column name matches table name pattern
        # (e.g., student_id is PRIMARY KEY in students table, FK in others)
        correct_table = None
        candidate_tables = []
        
        for col in missing_cols:
            for table_name, table_cols in schema_tables.items():
                if col in [c.lower() for c in table_cols]:
                    candidate_tables.append((table_name, col))
        
        if not candidate_tables:
            # Couldn't find the correct table
            logger.warning(f"Could not find table for columns: {missing_cols}")
            return sql
        
        # If multiple candidates, try to pick the best one
        if len(candidate_tables) > 1:
            # Prioritize table where column name matches table pattern
            # e.g., student_id matches students table (singular form)
            for table_name, col in candidate_tables:
                # Check if column is like "{table_singular}_id"
                table_singular = table_name.rstrip('s')  # students → student
                if col.replace('_id', '') == table_singular:
                    correct_table = table_name
                    logger.info(f"Selected '{table_name}' because column '{col}' matches table name pattern")
                    break
            
            # If still no match, check if question mentions the table/entity name
            if not correct_table:
                question_lower = question.lower()
                for table_name, col in candidate_tables:
                    if table_name in question_lower or table_name.rstrip('s') in question_lower:
                        correct_table = table_name
                        logger.info(f"Selected '{table_name}' because question mentions it")
                        break
        
        # If still no match, use first candidate
        if not correct_table:
            correct_table = candidate_tables[0][0]
        
        logger.info(f"SELECT columns {missing_cols} are in '{correct_table}', but FROM is '{from_table}' - rebuilding query")
        
        # Extract WHERE clause
        where_match = re.search(r'WHERE\s+(.+?)(?:GROUP|ORDER|LIMIT|;|$)', sql, re.IGNORECASE)
        where_clause = where_match.group(1).strip() if where_match else None
        
        # Find FK relationship between correct_table and from_table
        fk_rel = self._find_fk_relationship(correct_table, from_table, fk_map)
        
        if not fk_rel:
            # Try reverse direction
            fk_rel = self._find_fk_relationship(from_table, correct_table, fk_map)
            if fk_rel:
                # Swap direction
                temp = correct_table
                correct_table = from_table
                from_table = temp
        
        if fk_rel:
            # Build query with JOIN
            # Use simple aliases: main table = no alias, joined table = first letter
            join_table_alias = from_table[0]
            
            # Build SELECT with proper column references
            new_select_parts = []
            for col in select_cols:
                # Column is from correct_table (main table)
                new_select_parts.append(col)
            new_select = ', '.join(new_select_parts)
            
            # Build JOIN
            join_clause = f" JOIN {from_table} {join_table_alias} ON {correct_table}.{fk_rel['from_col']} = {join_table_alias}.{fk_rel['to_col']}"
            
            # Fix WHERE clause - add table prefix if needed
            new_where = where_clause
            if where_clause:
                # Add table alias to WHERE columns that belong to the joined table
                for col in schema_tables.get(from_table, []):
                    # Replace bare column with aliased version
                    new_where = re.sub(rf'\b{col}\b', f'{join_table_alias}.{col}', new_where, flags=re.IGNORECASE)
            
            # Construct final query
            new_sql = f"SELECT {new_select} FROM {correct_table}{join_clause}"
            if new_where:
                new_sql += f" WHERE {new_where}"
            
            logger.info(f"Fixed SELECT from wrong table:\n  Original: {sql}\n  Fixed: {new_sql}")
            return new_sql
        
        # No direct FK found - try multi-hop join path
        join_path = self._find_join_path(
            correct_table, from_table, fk_map, set(), question.lower()
        )
        if join_path:
            logger.info(f"Using multi-hop join path from {correct_table} to {from_table}")
            
            # Build multi-hop JOIN query
            # Use aliases: main table = first letter, intermediate tables = letters
            alias_map = {correct_table: correct_table[0]}
            current_alias = correct_table[0]
            
            # Build JOIN clauses
            join_clauses = []
            for i, step in enumerate(join_path):
                intermediate_table = step['table']
                # Create alias for intermediate table
                if intermediate_table not in alias_map:
                    alias_map[intermediate_table] = intermediate_table[0] if i == 0 else f"{intermediate_table[0]}{i}"
                
                intermediate_alias = alias_map[intermediate_table]
                from_tbl = step['from_table']
                from_alias = alias_map.get(from_tbl, from_tbl[0])
                
                # Build ON condition
                join_clauses.append(
                    f" JOIN {intermediate_table} {intermediate_alias} ON {from_alias}.{step['from_col']} = {intermediate_alias}.{step['to_col']}"
                )
            
            # Build SELECT - add student names if selecting student_id
            new_select_parts = []
            if 'student_id' in select_cols and correct_table == 'students':
                new_select_parts.extend(['first_name', 'last_name'])
            else:
                new_select_parts.extend(select_cols)
            new_select = ', '.join(new_select_parts)
            
            # Fix WHERE clause - add table prefix for columns from the original FROM table
            new_where = where_clause
            if where_clause:
                last_table_alias = alias_map[from_table]
                for col in schema_tables.get(from_table, []):
                    # Replace bare column with aliased version
                    new_where = re.sub(rf'\b{col}\b', f'{last_table_alias}.{col}', new_where, flags=re.IGNORECASE)
            
            # Construct final query
            new_sql = f"SELECT {new_select} FROM {correct_table} {current_alias}{''.join(join_clauses)}"
            if new_where:
                new_sql += f" WHERE {new_where}"
            
            logger.info(f"Fixed SELECT from wrong table with multi-hop JOIN:\n  Original: {sql}\n  Fixed: {new_sql}")
            return new_sql
        
        logger.warning(f"No FK relationship found between {correct_table} and {from_table}")
        return sql
    
    def _remove_incorrect_group_by(self, sql: str) -> str:
        """
        Remove GROUP BY clause when there's no aggregation function.
        
        The ML model sometimes incorrectly adds GROUP BY to simple SELECT queries.
        Pattern: SELECT col1, col2 FROM table GROUP BY col1
        Should be: SELECT col1, col2 FROM table
        
        Only remove if:
        - No aggregation functions (COUNT, SUM, AVG, MAX, MIN) in SELECT
        - GROUP BY is present
        """
        sql_upper = sql.upper()
        
        # Check if GROUP BY exists
        if 'GROUP BY' not in sql_upper:
            return sql
        
        # Check if there's an aggregation function in SELECT clause
        select_match = re.search(r'SELECT\s+(.*?)\s+FROM', sql, re.IGNORECASE | re.DOTALL)
        if not select_match:
            return sql
        
        select_clause = select_match.group(1).upper()
        
        # Check for aggregation functions
        has_aggregation = any(
            agg in select_clause 
            for agg in ['COUNT(', 'SUM(', 'AVG(', 'MAX(', 'MIN(', 'COUNT (', 'SUM (', 'AVG (', 'MAX (', 'MIN (']
        )
        
        # If no aggregation, remove GROUP BY
        if not has_aggregation:
            logger.info(f"Removing incorrect GROUP BY from query without aggregation: {sql}")
            sql = re.sub(r'\s+GROUP\s+BY\s+[^;]+', '', sql, flags=re.IGNORECASE)
        
        return sql
    
    def _fix_ambiguous_join_conditions(
        self,
        sql: str,
        schema_tables: dict,
        fk_map: dict
    ) -> str:
        """
        Fix JOIN conditions where column references are ambiguous (not qualified with table/alias).
        
        Example: JOIN courses AS T2 ON course_id = course_id
        Problem: Both sides reference course_id without specifying which table
        Fix: JOIN courses AS T2 ON T1.course_id = T2.course_id
        
        Strategy:
        1. Find JOIN clauses with unqualified column comparisons
        2. Determine which tables are being joined
        3. Check which tables have the column
        4. Add proper table/alias qualifiers
        """
        original_sql = sql
        
        # Pattern: JOIN table [AS] alias ON column = column (no table qualifiers)
        # This matches: JOIN courses T2 ON course_id = course_id
        ambiguous_pattern = r'(JOIN\s+(\w+)(?:\s+(?:AS\s+)?(\w+))?\s+ON\s+)(\w+)\s*=\s*(\4)'
        
        matches = list(re.finditer(ambiguous_pattern, sql, re.IGNORECASE))
        
        if not matches:
            return sql
        
        # Build alias to table mapping
        alias_to_table = {}
        
        # Find FROM table
        from_match = re.search(r'FROM\s+(\w+)(?:\s+(?:AS\s+)?(\w+))?', sql, re.IGNORECASE)
        if not from_match:
            return sql
        
        from_table = from_match.group(1).lower()
        from_alias = from_match.group(2).lower() if from_match.group(2) else from_table
        alias_to_table[from_alias] = from_table
        
        # Track all JOINed tables before this one (for context)
        all_join_matches = re.finditer(
            r'JOIN\s+(\w+)(?:\s+(?:AS\s+)?(\w+))?',
            sql,
            re.IGNORECASE
        )
        for jm in all_join_matches:
            j_table = jm.group(1).lower()
            j_alias = jm.group(2).lower() if jm.group(2) else j_table
            alias_to_table[j_alias] = j_table
        
        # Fix each ambiguous JOIN condition
        for match in matches:
            full_match = match.group(0)  # Full matched text
            join_prefix = match.group(1)  # "JOIN table alias ON "
            join_table = match.group(2).lower()  # The table being joined
            join_alias = match.group(3).lower() if match.group(3) else join_table  # The alias
            column_name = match.group(4).lower()  # The column name
            
            logger.info(f"Detected ambiguous JOIN condition: ON {column_name} = {column_name}")
            
            # Find which table to reference on the left side (the previously joined table)
            # This is typically the FROM table or the last table in the chain
            left_alias = from_alias
            
            # Check if the column exists in both tables
            left_table = alias_to_table.get(left_alias)
            right_table = alias_to_table[join_alias]
            
            left_has_col = left_table and left_table in schema_tables and column_name in [c.lower() for c in schema_tables[left_table]]
            right_has_col = right_table in schema_tables and column_name in [c.lower() for c in schema_tables[right_table]]
            
            if left_has_col and right_has_col:
                # Both tables have the column - qualify with proper aliases
                fixed_join = f"{join_prefix}{left_alias}.{column_name} = {join_alias}.{column_name}"
                sql = sql.replace(full_match, fixed_join)
                logger.info(f"Fixed ambiguous JOIN: {left_alias}.{column_name} = {join_alias}.{column_name}")
            elif not left_has_col or not right_has_col:
                # One or both don't have the column - try to find FK relationship
                fk_info = self._find_fk_relationship(left_table, right_table, fk_map)
                if fk_info:
                    fixed_join = f"JOIN {right_table} AS {join_alias} ON {left_alias}.{fk_info['from_col']} = {join_alias}.{fk_info['to_col']}"
                    # Replace just the JOIN part
                    old_join = match.group(0)
                    sql = sql.replace(old_join, fixed_join)
                    logger.info(f"Fixed ambiguous JOIN with FK: {left_alias}.{fk_info['from_col']} = {join_alias}.{fk_info['to_col']}")
        
        if sql != original_sql:
            logger.info(f"Fixed ambiguous JOIN conditions:\n  Original: {original_sql}\n  Fixed: {sql}")
        
        return sql
    
    def _fix_incorrect_join_conditions(
        self,
        sql: str,
        schema_tables: dict,
        fk_map: dict
    ) -> str:
        """
        Detect and fix incorrect JOIN conditions where the column doesn't exist in the referenced table.
        
        Example: SELECT ... FROM students AS T1 JOIN enrollments AS T2 ON T1.course_id = T2.course_id
        Problem: students table doesn't have course_id
        Fix: Replace with correct FK relationship (T1.student_id = T2.student_id)
        """
        # Find all JOIN conditions
        join_pattern = r'JOIN\s+(\w+)\s+(?:AS\s+)?(\w+)\s+ON\s+(\w+)\.(\w+)\s*=\s*(\w+)\.(\w+)'
        matches = list(re.finditer(join_pattern, sql, re.IGNORECASE))
        
        if not matches:
            return sql
        
        # Build alias to table mapping
        alias_to_table = {}
        
        # Find FROM table
        from_match = re.search(r'FROM\s+(\w+)(?:\s+(?:AS\s+)?(\w+))?', sql, re.IGNORECASE)
        if from_match:
            table = from_match.group(1).lower()
            alias = from_match.group(2).lower() if from_match.group(2) else table
            alias_to_table[alias] = table
        
        # Map all JOIN tables
        for match in matches:
            table = match.group(1).lower()
            alias = match.group(2).lower()
            alias_to_table[alias] = table
        
        # Check each JOIN condition
        for match in matches:
            left_alias = match.group(3).lower()
            left_col = match.group(4).lower()
            right_alias = match.group(5).lower()
            right_col = match.group(6).lower()
            
            if left_alias not in alias_to_table or right_alias not in alias_to_table:
                continue
            
            left_table = alias_to_table[left_alias]
            right_table = alias_to_table[right_alias]
            
            # Check if columns exist
            left_col_exists = left_table in schema_tables and left_col in [c.lower() for c in schema_tables[left_table]]
            right_col_exists = right_table in schema_tables and right_col in [c.lower() for c in schema_tables[right_table]]
            
            if not left_col_exists or not right_col_exists:
                logger.info(f"Detected incorrect JOIN condition: {left_alias}.{left_col} = {right_alias}.{right_col}")
                
                # Find correct FK relationship between these tables
                fk_info = self._find_fk_relationship(left_table, right_table, fk_map)
                if fk_info:
                    # Replace with correct JOIN condition
                    correct_join = f"JOIN {right_table} AS {right_alias} ON {left_alias}.{fk_info['from_col']} = {right_alias}.{fk_info['to_col']}"
                    old_join = match.group(0)
                    sql = sql.replace(old_join, correct_join)
                    logger.info(f"Fixed JOIN condition: {left_alias}.{fk_info['from_col']} = {right_alias}.{fk_info['to_col']}")
        
        return sql
    
    def _fix_missing_joins(
        self,
        sql: str,
        question: str,
        schema_tables: dict,
        fk_map: dict
    ) -> str:
        """
        Detect and fix missing JOINs.
        
        Pattern: If query references multiple tables but has no JOIN, add JOINs.
        """
        sql_upper = sql.upper()
        question_lower = question.lower()
        
        # Check if JOIN already exists
        if 'JOIN' in sql_upper:
            return sql  # Already has JOIN
        
        # Extract tables referenced in SQL (FROM, table.column)
        tables_in_sql = self._extract_tables_from_sql(sql, schema_tables)
        
        logger.info(f"Tables extracted from SQL: {tables_in_sql}")
        
        # Fix FROM clause if it references wrong table
        # This happens when model generates alias.column but wrong table in FROM
        from_match = re.search(r'FROM\s+(\w+)', sql, re.IGNORECASE)
        if from_match:
            current_from_table = from_match.group(1).lower()
            
            # Check if there's a different table referenced via columns that should be the main table
            # Priority: table with aggregation columns (e.g., gpa in AVG(s.gpa))
            if len(tables_in_sql) >= 2:
                # Find which table has the columns being aggregated/selected
                agg_match = re.search(r'(AVG|SUM|COUNT|MIN|MAX)\s*\(\s*([a-z])\.([\w_]+)\s*\)', sql, re.IGNORECASE)
                if agg_match:
                    alias, column = agg_match.group(2), agg_match.group(3).lower()
                    # Find table with this column
                    for table_name, columns in schema_tables.items():
                        if column in [c.lower() for c in columns] and table_name.lower() != current_from_table:
                            # Replace FROM clause with correct table
                            correct_table = table_name.lower()
                            sql = re.sub(
                                rf'FROM\s+{re.escape(current_from_table)}',
                                f'FROM {correct_table} {alias}',
                                sql,
                                flags=re.IGNORECASE
                            )
                            logger.info(f"Fixed FROM clause: {current_from_table} -> {correct_table} {alias}")
                            break
        
        # If multiple tables referenced, need JOIN
        if len(tables_in_sql) >= 2:
            logger.info(f"Multiple tables detected, injecting JOIN")
            return self._inject_join(sql, question_lower, schema_tables, fk_map, tables_in_sql)
        
        # Check for patterns that indicate missing JOIN
        # Pattern 1: "X with their Y" or "X with Y name"
        if re.search(r'\bwith\s+(their|its|the)\s+\w+', question_lower):
            return self._inject_join_from_question(sql, question_lower, schema_tables, fk_map)
        
        # Pattern 2: "X in/from Y department/category"
        if re.search(r'\b(in|from)\s+\w+\s+(department|category)', question_lower):
            return self._inject_join_from_question(sql, question_lower, schema_tables, fk_map)
        
        # Pattern 3: "how many X per/in each Y"
        if re.search(r'how\s+many.*\b(per|in\s+each|for\s+each)', question_lower):
            return self._inject_join_for_grouping(sql, question_lower, schema_tables, fk_map)
        
        # Pattern 4: "average/total X by Y"
        if re.search(r'(average|avg|total|sum).*\bby\s+\w+', question_lower):
            return self._inject_join_for_grouping(sql, question_lower, schema_tables, fk_map)
        
        return sql
    
    def _extract_tables_from_sql(self, sql: str, schema_tables: dict) -> set:
        """Extract all tables referenced in SQL"""
        tables = set()
        sql_lower = sql.lower()
        
        # Find FROM clause tables
        from_match = re.search(r'from\s+(\w+)', sql_lower)
        if from_match:
            tables.add(from_match.group(1))
        
        # Find JOIN clause tables
        join_matches = re.findall(r'join\s+(\w+)', sql_lower)
        for table_name in join_matches:
            tables.add(table_name)
        
        # Find table.column references (look for any tablename. pattern)
        for table_name in schema_tables.keys():
            # Match tablename followed by dot (for table.column)
            if re.search(rf'{re.escape(table_name)}\.', sql_lower):
                tables.add(table_name)
        
        # NEW: Detect aliases and infer table from column names
        # Pattern: alias.column_name (e.g., s.gpa, e.salary)
        alias_refs = re.findall(r'\b([a-z])\.([\w_]+)', sql_lower)
        
        for alias, column in alias_refs:
            # Check which table contains this column
            for table_name, columns in schema_tables.items():
                if column in [c.lower() for c in columns]:
                    tables.add(table_name.lower())
                    logger.info(f"Inferred table '{table_name}' from alias '{alias}' and column '{column}'")
                    break
        
        return tables

    def _build_alias_to_table_map(self, sql: str) -> Tuple[Dict[str, str], Dict[str, str]]:
        """
        Map alias -> physical table for FROM and JOIN clauses.
        Returns (alias_to_table, table_to_alias) using last alias if a table appears twice.
        """
        alias_to_table: Dict[str, str] = {}
        table_to_alias: Dict[str, str] = {}
        from_match = re.search(
            r'FROM\s+(\w+)(?:\s+(?:AS\s+)?(\w+))?(?=\s+(?:WHERE|JOIN|ORDER|GROUP|LIMIT|$))',
            sql,
            re.IGNORECASE,
        )
        if from_match:
            ft = from_match.group(1).lower()
            fa = from_match.group(2).lower() if from_match.group(2) else ft
            alias_to_table[fa] = ft
            table_to_alias[ft] = fa
        for match in re.finditer(
            r'JOIN\s+(\w+)(?:\s+(?:AS\s+)?(\w+))?(?=\s+(?:ON|WHERE|JOIN|ORDER|GROUP|LIMIT|$))',
            sql,
            re.IGNORECASE,
        ):
            jt = match.group(1).lower()
            ja = match.group(2).lower() if match.group(2) else jt
            alias_to_table[ja] = jt
            table_to_alias[jt] = ja
        return alias_to_table, table_to_alias

    def _question_mentions_schema_table(self, question_lower: str, table: str) -> bool:
        tl = table.lower()
        if tl in question_lower:
            return True
        if tl.endswith("s") and len(tl) > 1 and tl[:-1] in question_lower:
            return True
        if not tl.endswith("s") and f"{tl}s" in question_lower:
            return True
        return False

    def _display_columns_for_table(self, columns: List[str]) -> List[str]:
        """Pick human-facing columns from schema (no table-specific names)."""
        cols_lower = [c.lower() for c in columns]
        if "first_name" in cols_lower and "last_name" in cols_lower:
            return ["first_name", "last_name"]
        name_like = [
            c
            for c in columns
            if "name" in c.lower()
            and not c.lower().endswith("_id")
            and c.lower() not in ("filename", "username")
        ]
        if name_like:
            return [name_like[0].lower()] if len(name_like) == 1 else [c.lower() for c in name_like[:3]]
        title_like = [c for c in columns if "title" in c.lower() and not c.lower().endswith("_id")]
        if title_like:
            return [title_like[0].lower()]
        return []

    def _select_lists_columns_for_table(
        self,
        select_clause: str,
        table: str,
        alias: str,
        schema_columns: List[str],
    ) -> bool:
        """True if SELECT already projects at least one column from this table (via alias)."""
        sc = [c.lower() for c in schema_columns]
        for m in re.finditer(rf'\b{re.escape(alias)}\.(\w+)', select_clause, re.IGNORECASE):
            col = m.group(1).lower()
            if col in sc:
                return True
        return False

    def _expand_select_for_joined_tables(
        self, sql: str, question: str, schema_tables: dict
    ) -> str:
        """
        When NL asks for names from multiple joined tables but SELECT only lists one side,
        append display columns for missing tables using existing aliases (schema-driven).
        """
        if "JOIN" not in sql.upper():
            return sql
        qlow = question.lower()
        if not re.search(r"\bnames?\b", qlow):
            return sql
        if not re.search(r"\bwith\b", qlow):
            return sql

        select_match = re.search(
            r"\bSELECT\s+(DISTINCT\s+)?(.+?)\s+FROM\b",
            sql,
            re.IGNORECASE | re.DOTALL,
        )
        if not select_match:
            return sql
        select_clause = select_match.group(2).strip()
        if re.search(r"(?<!\.)\*\b", select_clause):
            return sql
        if re.search(r"\b(COUNT|AVG|SUM|MAX|MIN)\s*\(", select_clause, re.IGNORECASE):
            return sql

        alias_to_table, table_to_alias = self._build_alias_to_table_map(sql)
        joined_tables = set(alias_to_table.values())
        if len(joined_tables) < 2:
            return sql

        mentioned = {
            t.lower()
            for t in schema_tables
            if self._question_mentions_schema_table(qlow, t) and t.lower() in joined_tables
        }
        if len(mentioned) < 2:
            return sql

        additions: List[str] = []
        for table in sorted(mentioned):
            alias = table_to_alias.get(table)
            if not alias:
                continue
            cols = schema_tables.get(table, [])
            if not cols:
                continue
            if self._select_lists_columns_for_table(
                select_clause, table, alias, cols
            ):
                continue
            disp = self._display_columns_for_table(cols)
            for c in disp:
                qual = f"{alias}.{c}"
                if qual.lower() not in sql.lower():
                    additions.append(qual)

        if not additions:
            return sql

        new_select = f"{select_clause}, {', '.join(additions)}"
        sql = re.sub(
            r"\bSELECT\s+.*?\s+FROM\b",
            f"SELECT {new_select} FROM",
            sql,
            count=1,
            flags=re.IGNORECASE | re.DOTALL,
        )
        logger.info("Expanded SELECT for joined tables: added %s", additions)
        return sql
    
    def _inject_join_from_question(
        self,
        sql: str,
        question: str,
        schema_tables: dict,
        fk_map: dict
    ) -> str:
        """Inject JOIN by analyzing question for table references"""
        # Find main table from FROM clause
        from_match = re.search(r'from\s+(\w+)', sql, re.IGNORECASE)
        if not from_match:
            return sql
        
        main_table = from_match.group(1).lower()
        
        # Find target table mentioned in question
        # Check both exact match and partial match (e.g., "department" matches "departments")
        target_table = None
        for table_name in schema_tables.keys():
            table_lower = table_name.lower()
            if table_lower != main_table:
                # Exact match
                if table_lower in question:
                    target_table = table_lower
                    break
                # Partial match: remove trailing 's' and check
                if table_lower.endswith('s'):
                    singular = table_lower[:-1]
                    if singular in question:
                        target_table = table_lower
                        break
        
        if not target_table:
            ql = question.lower()
            for ent_pat, attr_pat, detail_table in self.entity_patterns:
                if detail_table not in schema_tables:
                    continue
                if re.search(ent_pat, ql) and re.search(attr_pat, ql):
                    target_table = detail_table
                    break
        
        if not target_table:
            return sql
        
        # Add missing column from target table to SELECT if needed
        # Pattern: "professors with their department names" → need departments.department_name in SELECT
        if target_table in schema_tables:
            target_columns = schema_tables[target_table]
            # Find name column in target table
            name_cols = [col for col in target_columns if 'name' in col.lower()]
            if name_cols:
                needed_col = f"{target_table}.{name_cols[0]}"
                # Check if this column is already in SELECT
                if needed_col not in sql.lower():
                    # Add it to SELECT clause
                    select_match = re.search(r'(SELECT\s+)(.*?)(\s+FROM)', sql, re.IGNORECASE)
                    if select_match:
                        existing_select = select_match.group(2)
                        new_select = f"{existing_select}, {needed_col}"
                        sql = re.sub(
                            r'SELECT\s+.*?\s+FROM',
                            f'SELECT {new_select} FROM',
                            sql,
                            count=1,
                            flags=re.IGNORECASE
                        )
                        logger.info(f"Added {needed_col} to SELECT clause")
        
        tables_to_join = {main_table, target_table}
        return self._inject_join(sql, question, schema_tables, fk_map, tables_to_join)
    
    def _inject_join(
        self,
        sql: str,
        question: str,
        schema_tables: dict,
        fk_map: dict,
        tables_in_sql: set
    ) -> str:
        """
        Inject JOIN clause into SQL based on foreign key relationships.
        
        Args:
            tables_in_sql: Set of tables that need to be joined
        """
        # Parse current SQL to find main table
        from_match = re.search(r'FROM\s+(\w+)', sql, re.IGNORECASE)
        if not from_match:
            return sql
        
        main_table = from_match.group(1).lower()
        tables_list = list(tables_in_sql)
        
        # Find other table(s) to join
        other_tables = [t for t in tables_list if t != main_table]
        if not other_tables:
            return sql
        
        # Build JOIN clause for each table
        join_clause = ""
        joined_tables = {main_table}  # Track which tables we've already joined
        
        for target_table in other_tables:
            # Try direct FK relationship first
            fk_info = self._find_fk_relationship(main_table, target_table, fk_map)
            if fk_info:
                # Direct relationship found
                if target_table not in joined_tables:
                    join_clause += f" JOIN {target_table} ON {fk_info['from_table']}.{fk_info['from_col']} = {fk_info['to_table']}.{fk_info['to_col']}"
                    joined_tables.add(target_table)
            else:
                # Try multi-hop path (e.g., courses -> enrollments -> grades)
                qlow = (question or "").lower()
                path = self._find_join_path(
                    main_table, target_table, fk_map, joined_tables, qlow
                )
                if path:
                    for step in path:
                        if step['table'] not in joined_tables:
                            join_clause += f" JOIN {step['table']} ON {step['from_table']}.{step['from_col']} = {step['table']}.{step['to_col']}"
                            joined_tables.add(step['table'])
        
        if not join_clause:
            return sql
        
        # Inject JOIN after FROM clause (including optional alias)
        # CRITICAL: Don't match 'G' from 'GROUP BY' as an alias
        # An alias is only valid if it's NOT followed by 'ROUP' (completing GROUP)
        
        # Find the FROM clause and any valid alias
        from_pattern = r'FROM\s+(\w+)(?:\s+([a-z])(?!ROUP\s+BY))?'
        from_match = re.search(from_pattern, sql, re.IGNORECASE)
        
        if from_match:
            from_table = from_match.group(1)
            alias = from_match.group(2) if from_match.group(2) else ""
            
            # Build replacement: FROM table [alias] JOIN ...
            replacement = f'FROM {from_table}'
            if alias:
                replacement += f' {alias}'
            replacement += join_clause
            
            # Replace the FROM clause
            sql = re.sub(
                from_pattern,
                replacement,
                sql,
                count=1,
                flags=re.IGNORECASE
            )
        
        return sql
    
    def _find_fk_relationship(self, table1: str, table2: str, fk_map: dict) -> Optional[dict]:
        """Find FK relationship between two tables (either direction)"""
        # Check table1 -> table2
        for fk in fk_map.get(table1, []):
            if fk['to_table'].lower() == table2:
                return {
                    'from_table': table1,
                    'from_col': fk['from_col'],
                    'to_table': table2,
                    'to_col': fk['to_col']
                }
        
        # Check table2 -> table1
        for fk in fk_map.get(table2, []):
            if fk['to_table'].lower() == table1:
                return {
                    'from_table': table2,
                    'from_col': fk['from_col'],
                    'to_table': table1,
                    'to_col': fk['to_col']
                }
        
        return None
    
    def _find_join_path(
        self,
        from_table: str,
        to_table: str,
        fk_map: dict,
        existing_joins: set,
        question_lower: str = "",
    ) -> Optional[List[dict]]:
        """
        Find a multi-hop JOIN path between two tables.
        
        For example: courses -> enrollments -> grades
        
        Returns list of join steps if path found, None otherwise.
        Each step is: {'table': 'table_name', 'from_table': 'x', 'from_col': 'y', 'to_col': 'z'}
        """
        # Common patterns for student_db
        join_patterns = {
            ('courses', 'grades'): [
                {'table': 'enrollments', 'from_table': 'courses', 'from_col': 'course_id', 'to_col': 'course_id'},
                {'table': 'grades', 'from_table': 'enrollments', 'from_col': 'enrollment_id', 'to_col': 'enrollment_id'}
            ],
            ('grades', 'courses'): [
                {'table': 'enrollments', 'from_table': 'grades', 'from_col': 'enrollment_id', 'to_col': 'enrollment_id'},
                {'table': 'courses', 'from_table': 'enrollments', 'from_col': 'course_id', 'to_col': 'course_id'}
            ],
            ('students', 'grades'): [
                {'table': 'enrollments', 'from_table': 'students', 'from_col': 'student_id', 'to_col': 'student_id'},
                {'table': 'grades', 'from_table': 'enrollments', 'from_col': 'enrollment_id', 'to_col': 'enrollment_id'}
            ],
            ('grades', 'students'): [
                {'table': 'enrollments', 'from_table': 'grades', 'from_col': 'enrollment_id', 'to_col': 'enrollment_id'},
                {'table': 'students', 'from_table': 'enrollments', 'from_col': 'student_id', 'to_col': 'student_id'}
            ],
        }
        
        # Prefer hand-tuned paths when present (same length semantics as historical behavior)
        key = (from_table.lower(), to_table.lower())
        if key in join_patterns:
            return join_patterns[key]

        prefer = self._prefer_enrollment_bridge_students_courses(
            from_table, to_table, question_lower
        )
        return self._bfs_join_path_steps(
            from_table, to_table, fk_map, prefer_enrollment_students_courses=prefer
        )
    
    def _inject_join_for_grouping(
        self,
        sql: str,
        question: str,
        schema_tables: dict,
        fk_map: dict
    ) -> str:
        """
        Add JOIN when query needs grouping by a dimension from another table.
        
        Example: "How many students in each department?"
        - Main table: students
        - Group by: department.department_name (from departments table)
        - Need JOIN: students → departments
        """
        from_match = re.search(r'FROM\s+(\w+)', sql, re.IGNORECASE)
        if not from_match:
            return sql
        
        main_table = from_match.group(1).lower()
        
        # Detect grouping dimension from question
        grouping_keywords = ['per', 'each', 'by', 'in each', 'for each']
        grouping_entity = None
        
        for keyword in grouping_keywords:
            pattern = rf'{keyword}\s+(\w+)'
            match = re.search(pattern, question, re.IGNORECASE)
            if match:
                grouping_entity = match.group(1).lower()
                break
        
        if not grouping_entity:
            return sql
        
        # Find table that contains this grouping entity
        target_table = None
        for table_name in schema_tables.keys():
            if grouping_entity in table_name.lower() or table_name.lower().startswith(grouping_entity[:-1]):  # Handle plural
                target_table = table_name.lower()
                break
        
        if not target_table or target_table == main_table:
            return sql
        
        # Find FK relationship
        if main_table not in fk_map:
            return sql
        
        fk_info = None
        for fk in fk_map.get(main_table, []):
            if fk['to_table'].lower() == target_table:
                fk_info = fk
                break
        
        if not fk_info:
            return sql
        
        # Build corrected SQL with JOIN and GROUP BY
        join_clause = f" JOIN {target_table} ON {main_table}.{fk_info['from_col']} = {target_table}.{fk_info['to_col']}"
        
        # Find name column in target table
        name_cols = [col for col in schema_tables.get(target_table, []) if 'name' in col.lower()]
        if not name_cols:
            name_cols = [schema_tables[target_table][1]] if len(schema_tables[target_table]) > 1 else [schema_tables[target_table][0]]
        
        grouping_col = f"{target_table}.{name_cols[0]}"
        
        # Rebuild query
        select_match = re.search(r'SELECT\s+(.*?)\s+FROM', sql, re.IGNORECASE | re.DOTALL)
        if select_match:
            # Keep aggregation, add grouping column
            agg_part = select_match.group(1)
            new_select = f"{grouping_col}, {agg_part}" if grouping_col not in agg_part else agg_part
            
            # Remove any existing GROUP BY (likely wrong)
            sql = re.sub(r'\s+GROUP\s+BY.*', '', sql, flags=re.IGNORECASE)
            
            # Rebuild
            sql = f"SELECT {new_select} FROM {main_table}{join_clause} GROUP BY {grouping_col}"
        
        return sql
    
    def _fix_where_clause_issues(
        self,
        sql: str,
        question: str,
        schema_tables: dict,
        fk_map: dict
    ) -> str:
        """
        Fix WHERE clause issues:
        1. Column referenced doesn't exist in the joined tables
        2. Wrong table alias used
        3. Value compared against wrong column (e.g., professor name in course_name)
        4. WHERE clause used when query actually needs JOIN (e.g., "grades with student names")
        
        Example issue: "courses taught by professor Hopper"
        Wrong: WHERE T2.course_name = 'Hopper'
        Right: WHERE p.last_name = 'Hopper' (need to join professors table)
        """
        # Check for pattern: "X with Y names" where query has WHERE instead of JOIN
        # Example: "List grades with student names" → should JOIN students, not WHERE student
        if re.search(r'\bwith\s+(\w+)\s+names?\b', question, re.IGNORECASE):
            match = re.search(r'\bwith\s+(\w+)\s+names?\b', question, re.IGNORECASE)
            if match:
                entity = match.group(1).lower()  # "student", "professor", etc.
                
                # Check if query has WHERE clause that looks wrong
                if 'WHERE' in sql.upper() and 'JOIN' not in sql.upper():
                    # Find which table the entity refers to
                    target_table = None
                    for table_name in schema_tables.keys():
                        # Skip if table_name contains underscore (likely database name like student_db)
                        if '_' in table_name and 'db' in table_name.lower():
                            continue
                        # Check if entity matches table name (singular or plural)
                        table_singular = table_name.rstrip('s').lower()
                        if entity == table_name.lower() or entity == table_singular or entity + 's' == table_name.lower():
                            target_table = table_name
                            break
                    
                    if target_table:
                        logger.info(f"Detected 'X with Y names' pattern - rebuilding query with JOIN to {target_table}")
                        return self._rebuild_query_for_with_pattern(sql, question, target_table, schema_tables, fk_map)
        
        # Find WHERE clause
        where_match = re.search(r'WHERE\s+(.+?)(?:GROUP|ORDER|LIMIT|;|$)', sql, re.IGNORECASE)
        if not where_match:
            return sql
        
        where_clause = where_match.group(1).strip()
        
        # Extract all table aliases used in FROM/JOIN
        tables_in_query = {}  # alias -> table_name
        
        # Find FROM table
        from_match = re.search(r'FROM\s+(\w+)(?:\s+(?:AS\s+)?([A-Z0-9]+))?', sql, re.IGNORECASE)
        if from_match:
            table = from_match.group(1).lower()
            alias = from_match.group(2).lower() if from_match.group(2) else table
            tables_in_query[alias] = table
        
        # Find JOINed tables
        join_matches = re.finditer(r'JOIN\s+(\w+)(?:\s+(?:AS\s+)?([A-Z0-9]+))?(?=\s+(?:ON|WHERE|JOIN|ORDER|GROUP|LIMIT|$))', sql, re.IGNORECASE)
        for match in join_matches:
            table = match.group(1).lower()
            alias = match.group(2).lower() if match.group(2) else table
            tables_in_query[alias] = table
        
        # Check if WHERE clause references columns that don't exist
        # Pattern: alias.column or just column
        where_conditions = re.findall(r'(\w+)\.(\w+)\s*=\s*[\'"]([^\'"]+)[\'"]', where_clause, re.IGNORECASE)
        
        for alias, column, value in where_conditions:
            alias_lower = alias.lower()
            column_lower = column.lower()
            
            # Check if this alias exists
            if alias_lower not in tables_in_query:
                logger.info(f"WHERE clause references unknown alias '{alias}'")
                continue
            
            actual_table = tables_in_query[alias_lower]
            
            # Check if column exists in that table
            if actual_table in schema_tables:
                table_columns = [c.lower() for c in schema_tables[actual_table]]
                
                if column_lower not in table_columns:
                    logger.info(f"Column '{column}' not found in table '{actual_table}'. Looking for correct table...")
                    
                    # Try to infer correct table and column from question and value
                    # Require explicit instructor semantics (avoid rebuilding on stray Title Case literals)
                    needs_professor_filter = bool(
                        re.search(r'\b(?:professor|instructor)\b', question, re.IGNORECASE)
                        or re.search(r'\btaught\s+by\b', question, re.IGNORECASE)
                    )
                    
                    if needs_professor_filter:
                        # Should use professors table
                        if 'professors' in schema_tables:
                            prof_cols = schema_tables['professors']
                            
                            # Check if we need to add JOIN to professors
                            if 'professors' not in [t for t in tables_in_query.values()]:
                                # Need to add professors JOIN
                                # Find path to professors table
                                missing_tables = self._extract_tables_from_sql(sql, schema_tables)
                                missing_tables.add('professors')
                                
                                # Rebuild with correct JOINs
                                logger.info("Adding missing professors table for WHERE clause")
                                return self._rebuild_query_with_professor_filter(
                                    sql, question, value, schema_tables, fk_map
                                )
        
        return sql
    
    def _rebuild_query_for_with_pattern(
        self,
        sql: str,
        question: str,
        target_table: str,
        schema_tables: dict,
        fk_map: dict
    ) -> str:
        """
        Rebuild query for "X with Y names" pattern.
        
        Examples:
        - "List grades with student names" → grades JOIN enrollments JOIN students
        - "Show employees with department names" → employees JOIN departments
        """
        # Determine main table from existing SQL
        from_match = re.search(r'FROM\s+(\w+)', sql, re.IGNORECASE)
        if not from_match:
            logger.warning("Cannot rebuild query - no FROM table found")
            return sql
        
        main_table = from_match.group(1).lower()
        logger.info(f"Rebuilding '{main_table} with {target_table}' query")
        
        # Get columns from main table and target table
        main_cols = schema_tables.get(main_table, [])
        target_cols = schema_tables.get(target_table, [])
        
        if not main_cols or not target_cols:
            logger.warning(f"Cannot rebuild - missing schema for {main_table} or {target_table}")
            return sql
        
        # Build SELECT clause based on question
        select_cols = []
        
        # If "names" in question, add name columns from target table
        if 'names' in question.lower() or 'name' in question.lower():
            # Find name columns in target table
            name_cols = [col for col in target_cols if 'name' in col.lower()]
            if name_cols:
                select_cols.extend([f"{target_table[0]}.{col}" for col in name_cols])
        
        # Add main data columns (e.g., score from grades, salary from employees)
        # Common data columns to look for
        data_keywords = ['score', 'grade', 'salary', 'amount', 'value', 'quantity', 'count']
        for col in main_cols:
            if any(keyword in col.lower() for keyword in data_keywords):
                select_cols.append(f"{main_table[0]}.{col}")
        
        # If no data columns found, add first few non-ID columns
        if len(select_cols) == 0 or (len(select_cols) == len([c for c in select_cols if target_table[0] in c])):
            for col in main_cols:
                if 'id' not in col.lower() and col.lower() not in ['created_at', 'updated_at']:
                    select_cols.append(f"{main_table[0]}.{col}")
                    if len(select_cols) >= 3:
                        break
        
        if not select_cols:
            select_cols = [f"{main_table[0]}.*"]
        
        # Build JOIN clause using existing logic
        join_clause = ""
        joined_tables = {main_table}
        
        # Find path from main_table to target_table
        path = self._find_join_path(
            main_table, target_table, fk_map, joined_tables, question.lower()
        )
        if path:
            for step in path:
                if step['table'] not in joined_tables:
                    # Use alias (first letter) for consistency
                    from_alias = step['from_table'][0]
                    to_alias = step['table'][0]
                    join_clause += f" JOIN {step['table']} {to_alias} ON {from_alias}.{step['from_col']} = {to_alias}.{step['to_col']}"
                    joined_tables.add(step['table'])
        else:
            logger.warning(f"No JOIN path found from {main_table} to {target_table}")
            return sql
        
        # Construct new query
        new_sql = f"SELECT {', '.join(select_cols)} FROM {main_table} {main_table[0]}{join_clause}"
        
        logger.info(f"Rebuilt query: {new_sql}")
        return new_sql
    
